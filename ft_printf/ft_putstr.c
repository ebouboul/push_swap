#include "ft_printf.h"

void	ft_putstr_l(char *s, int *sum)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (s[i])
	{
		ft_putchar_l(s[i], sum);
		i++;
	}
}
